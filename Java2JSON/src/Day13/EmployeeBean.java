package Day13;

import java.io.Serializable;

public class EmployeeBean implements Serializable {
//	private int eid;
	private transient int eid; //cannot be accessed over serialization
	private String ename;
	private int esal;
	public EmployeeBean(int eid, String ename, int esal) {
		super();
		this.eid = eid;
		this.ename = ename;
		this.esal = esal;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public int getEsal() {
		return esal;
	}
	public void setEsal(int esal) {
		this.esal = esal;
	}
	
}
